package edu.mum.cs.cs525.labs.skeleton.command_pattern;

public class GarageDoor {
    public void up(){
        System.out.println("Garage Door is up");
    }
    public void down(){
        System.out.println("Garage Door is down");
    }
    public void stop(){
        System.out.println("Garage Door is stop");
    }
    public void lightOpen(){
        System.out.println("Garage Door lightOpen");
    }
    public void lightClose(){
        System.out.println("Garage Door lightClose");
    }
}
